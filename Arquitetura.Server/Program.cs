using Arquitetura.Application;
using Arquitetura.Infrastructure;
using Arquitetura.Server.Endpoints;
using Arquitetura.Server.Middleware;

var builder = WebApplication.CreateBuilder(args);

// ?? Camadas ??????????????????????????????????????????????????????????????????
builder.Services.AddApplicationServices();
builder.Services.AddInfrastructureServices(builder.Configuration);

// ?? API ???????????????????????????????????????????????????????????????????????
builder.Services.AddOpenApi();

var app = builder.Build();

// ?? Pipeline ?????????????????????????????????????????????????????????????????
app.UseMiddleware<ExceptionHandlingMiddleware>();

app.UseDefaultFiles();
app.MapStaticAssets();

if (app.Environment.IsDevelopment())
    app.MapOpenApi();

app.UseHttpsRedirection();

// ?? Endpoints ?????????????????????????????????????????????????????????????????
app.MapGroup("/api/produtos")
    .WithTags("Produtos")
    .MapEndpoints(new ProdutosEndpoints());

app.MapFallbackToFile("/index.html");

app.Run();

static partial class RouteGroupBuilderExtensions
{
    public static RouteGroupBuilder MapEndpoints(this RouteGroupBuilder group, IEndpointGroup endpoints)
    {
        endpoints.Map(group);
        return group;
    }
}
